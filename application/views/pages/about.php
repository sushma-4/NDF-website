                    <p align="left"><h4><b>Schemes under the National Defence Fund:</b></h4>
                    A scholarship scheme to encourage technical and post graduation education for the widows and wards of the deceased personnel of Armed Forces and Para Military Forces is being implemented. The scheme is being implemented by the Department of Ex-Servicemen Welfare, Ministry of Defence in respect of armed forces. In so far as personnel of paramilitary forces and Railway Protection force are concerned, the scheme is being implemented by Ministry of Home Affairs and Ministry of Railways respectively.</p>

                    <p align="left"><h4><b>Salient Features of “PM’s Scholarship Scheme” being implemented out of National Defence Fund:-</b></h4>
                    <ol type="1">
                      <li>The scheme applies to armed forces (including paramilitary forces). Monthly scholarships are awarded to wards of: </li>
                        <ol type="a">
                          <li>Ex-service personnel (below officer rank only),</li>
                          <li>their widows,</li>
                          <li> widows of personnel died in harness due to causes attributable to line of duty and</li>
                          <li> wards and widows of in service personnel of paramilitary forces and Railway Protection Force. Scholarships are available for education at technical institutions (medical, dental, veterinary, engineering, MBA, MCA, and other equivalent technical professions with suitable AICTE/UGC approval). For wards of widows and widows of personnel died in harness due to causes attributable as at (b) and (c) above, there are no rank restrictions. The scheme extends to the wards of all para-military forces also. Under the scheme 4000 new scholarships are given every year for wards of ex-servicemen of forces controlled by Ministry of Defence, 910 new scholarships for wards of forces controlled by Ministry of Home Affairs and 90 new scholarships for the wards of forces controlled by Ministry of Railways. However, from the academic year 2015-16, the number of new scholarships has been enhanced to 5500 for wards of ex-servicemen of forces controlled by Ministry of Defence, 2000 for wards of forces controlled by Ministry of Home Affairs and 150 for the wards of the forces controlled by Ministry of Railways. Initially, the scholarship rate was Rs. 1250 per month for boys and Rs. 1500 per month for girls. The annual scholarships rates have been revised to Rs. 2000 per month for boys and Rs. 2250 per month for girls.</li>
                        </ol>
                       <li> Annual grant of Rs. 15 lakh from NDF is being released to the SPG Family Welfare Fund to undertake various welfare activities for the benefit of its personnel and their families.</li>
