            <p align="center">The National Defence Fund was set up to take charge of voluntary donations in cash and kind received for promotion of the national defence effort, and to decide on their utilisation. The Fund is used for the welfare of the members of the Armed Forces (including Para Military Forces) and their dependents. The Fund is administered by an Executive Committee, with PM as Chairperson, and Defence, Finance and Home Ministers as Members. Finance Minister is the Treasurer of the Fund and the Joint Secretary, PMO dealing with the subject is Secretary of the Executive Committee. Accounts of the Fund are kept with the Reserve Bank of India. The fund is entirely dependent on voluntary contributions from the public and does not get any budgetary support. The fund accepts online contributions. Such contributions can be made through the website i.e. ndf.gov.in, pmindia.gov.in and www.onlinesbi.com website of State Bank of India. Collection account number is 11084239799 with State Bank of India, Institutional Division, 4th Floor, Parliament Street, New Delhi. <br> <font color="brown">The fund has also been allotted a Permanent Account Number (PAN) AAAGN0009F. </font><br><br>

